package utils;

import org.apache.commons.math3.linear.RealMatrix;

public class LinearSeparator {
	
	RealMatrix theta;
	double theta_0;
	
	public LinearSeparator (RealMatrix theta, double theta_0) {
		this.theta = theta;
		this.theta_0 = theta_0;
	}

	public RealMatrix theta() {
		return theta.copy();
	}
	
	public double theta_0() {
		return theta_0;
	}
}
